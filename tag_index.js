function convertTags(md) {

  return md.replace(/tags:\s*(.+)/g, (match, tagStr) => {

    const tags = tagStr.split(",").map(t => t.trim());

    const converted = tags.map(t => tagMap[t] || t);

    return "tags: " + converted.join(", ");
  });

}